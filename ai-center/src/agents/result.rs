use serde::{Deserialize, Serialize};
use uuid::Uuid;

/// Result of an agent execution — what the agent decided to do.
#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct AgentResult {
    /// Name of the agent that produced this result
    pub agent_name: String,
    /// Actions the agent recommends taking
    pub actions: Vec<AgentAction>,
    /// Notifications to send
    pub notifications: Vec<AgentNotification>,
    /// How confident the agent is in its analysis (0.0 - 1.0)
    pub confidence_score: f64,
    /// Human-readable summary of the analysis
    pub summary: String,
    /// Raw AI response for debugging
    pub raw_response: Option<String>,
    /// Processing time in milliseconds
    pub latency_ms: u64,
    /// Whether an AI provider was used (vs rule-based analysis)
    pub ai_provider_used: Option<String>,
}

impl AgentResult {
    /// Create a no-op result (agent decided not to act).
    pub fn no_action(agent_name: &str, reason: &str) -> Self {
        Self {
            agent_name: agent_name.to_string(),
            actions: Vec::new(),
            notifications: Vec::new(),
            confidence_score: 0.0,
            summary: reason.to_string(),
            raw_response: None,
            latency_ms: 0,
            ai_provider_used: None,
        }
    }

    /// Whether this result has any actions or notifications.
    pub fn has_actions(&self) -> bool {
        !self.actions.is_empty() || !self.notifications.is_empty()
    }
}

/// An action recommended by an agent.
#[derive(Debug, Clone, Serialize, Deserialize)]
#[serde(tag = "type", content = "params")]
pub enum AgentAction {
    /// Send a notification to a Slack channel
    NotifySlack {
        channel: String,
        message: String,
        urgency: String,
    },
    /// Send an email notification
    SendEmail {
        to: String,
        subject: String,
        body: String,
    },
    /// Update a CRM record (e.g., add tags, update segment)
    UpdateCRM {
        customer_id: String,
        field: String,
        value: serde_json::Value,
    },
    /// Retry the delivery later with adjusted parameters
    RetryLater {
        delay_seconds: i64,
        reason: String,
    },
    /// Block an endpoint temporarily or permanently
    BlockEndpoint {
        endpoint_id: Uuid,
        reason: String,
        duration_minutes: Option<i32>,
    },
    /// Call a custom webhook URL
    CustomWebhook {
        url: String,
        method: String,
        payload: serde_json::Value,
    },
    /// Add item to watchlist for monitoring
    AddToWatchlist {
        watch_type: String,
        value: String,
        reason: String,
    },
    /// Flag an order for manual review
    HoldOrder {
        order_id: String,
        reason: String,
    },
    /// Trigger a targeted campaign
    TriggerCampaign {
        campaign_type: String,
        customer_id: String,
        segment: String,
    },
}

/// A notification generated by an agent.
#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct AgentNotification {
    /// Channel to notify (slack, email, webhook)
    pub channel: String,
    /// Notification level (info, warning, critical)
    pub level: String,
    /// Title
    pub title: String,
    /// Message body
    pub message: String,
    /// Optional metadata
    pub metadata: Option<serde_json::Value>,
}

impl AgentNotification {
    pub fn slack(level: &str, title: &str, message: &str) -> Self {
        Self {
            channel: "slack".to_string(),
            level: level.to_string(),
            title: title.to_string(),
            message: message.to_string(),
            metadata: None,
        }
    }

    pub fn email(level: &str, title: &str, message: &str) -> Self {
        Self {
            channel: "email".to_string(),
            level: level.to_string(),
            title: title.to_string(),
            message: message.to_string(),
            metadata: None,
        }
    }

    pub fn critical_slack(title: &str, message: &str) -> Self {
        Self::slack("critical", title, message)
    }

    pub fn warning_slack(title: &str, message: &str) -> Self {
        Self::slack("warning", title, message)
    }
}
